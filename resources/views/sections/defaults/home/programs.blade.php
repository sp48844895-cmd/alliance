          <span class="program-tag">Dantewada · Nutrition &amp; Health</span>
          <h3>Bapi Na Uwat</h3>
          <p>Bapi Na Uwat is an innovative community-led SBC initiative launched in Dantewada by the district administration and UNICEF to reduce malnutrition and improve health behaviours in tribal communities.</p>
        </div>
        <div class="program-card-copy">
          <p>The initiative uses trusted elderly women, known as “Bapis,” to spread awareness on nutrition, breastfeeding, maternal care, and child health through local traditions and conversations. Implemented across 143 gram panchayats, the programme combines traditional wisdom with behaviour change communication tools such as local language videos, chaupals, and community engagement activities.</p>
          <p>Supported by village volunteers and frontline workers, the campaign has helped strengthen trust, awareness, and community participation around health and nutrition practices in remote areas of Bastar.</p>
        </div>
      </article>

      <article class="program-card" data-aos="fade-up" data-aos-delay="80">
        <span class="program-tag">Youth movement</span>
        <h3>Yuvoday</h3>
        <p>Yuvoday is a youth-led volunteer movement launched in Chhattisgarh with support from district administrations and UNICEF to strengthen community participation and behaviour change. Meaning “Rise of the Youth,” the initiative has built a network of over 12,000 volunteers who work across villages, urban wards, and tribal communities.</p>
        <p>Yuvoday volunteers support campaigns related to health, nutrition, sanitation, education, mental health, social protection, and COVID-19 awareness. By connecting youth with communities and frontline workers, the programme promotes local leadership, trust-building, and people-centered development.</p>
      </article>

      <article class="program-card program-card--teal" data-aos="fade-up" data-aos-delay="160">
        <span class="program-tag">Bijapur · Youth messengers</span>
        <h3>BijaDuteer</h3>
        <p>BijaDuteer is a youth volunteer initiative in Bijapur supported by the District Administration, UNICEF, and Chhattisgarh Agricon Samiti. In the local dialect, “Bija” refers to Bijapur and “Duteer” means messenger, representing a youth brigade working as community messengers for positive change.</p>
        <p>In this naxal-affected and remote district, BijaDuteer volunteers help bridge the gap between communities and governance by promoting awareness on health, nutrition, education, child rights, mental wellbeing, and positive parenting.</p>
      </article>

      <article class="program-card program-card--ochre" data-aos="fade-up" data-aos-delay="240">
        <span class="program-tag">Jashpur · Youth leadership</span>
        <h3>JAY HO!</h3>
        <p>JAY HO, the Jashpur Alliance of Youth for Hope and Opportunity, is a youth empowerment initiative in Jashpur launched by the District Administration and UNICEF to support adolescent wellbeing and positive behaviour change.</p>
        <p>The initiative focuses on adolescent health, life skills, online safety, safe migration, mental wellbeing, anaemia, child marriage, and substance abuse through youth-led engagement, peer learning, and community participation.</p>
      </article>

      <article class="program-card program-card--leaf" data-aos="fade-up" data-aos-delay="320">
        <span class="program-tag">Knowledge sharing</span>
        <h3>Learning Corners</h3>
        <p>The Learning Corner is a shared resource space under Alliance for Behaviour Change where IEC materials, training modules, toolkits, campaign resources, and communication materials are made accessible for learning, knowledge sharing, community mobilization, and awareness generation.</p>
        <p>The platform encourages individuals and organizations to freely explore, download, and use these resources to strengthen Social and Behaviour Change Communication efforts across communities.</p>
